export class TokenClass {
    private _token: string;

    constructor(token: string, trueForValidateFalseForGenerate: boolean) {
        if (!trueForValidateFalseForGenerate) {
            this._token = this.normalizeToken(token);
            return
        }

        if (!this.isValidToken(token)) {
            throw new DatatypeValidationError("Token", token);
        }

        this._token = token;
    }

    public get token(): string {
        return this._token;
    }

    public set token(token: string) {
        if (!this.isValidToken(token)) {
            throw new DatatypeValidationError("Token", token);
        }

        this._token = token;
    }

    private normalizeToken(value: string): string {
        return value.replace(/[\t\n\r]+/g, ' ').trim().replace(/\s+/g, ' ');
    }

    private isValidToken(value: string): boolean {
        const tokenRegex = /^[^\s]+(\s[^\s]+)*$/;
        return tokenRegex.test(value);
    }
}

